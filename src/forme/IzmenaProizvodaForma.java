    private void srediFormu() {
        txtNaziv.setText(proizvod.getNazivProizvoda());
        txtCena.setText(proizvod.getCena()+"");
        txtOpis.setText(proizvod.getOpis());
        cmbProizvodjaci.setSelectedItem(proizvod.getProizvodjac());
    }

    private void popuniProizvodjace() {
        ServerskiOdgovor so=Kontroler.getInstanca().vratiProizvodjace();
        if(so.isUspesno()){
          List<IObjekat> lista = (List<IObjekat>) so.getOdgovor();
          cmbProizvodjaci.removeAllItems();
          for (IObjekat iObjekat : lista) 
                cmbProizvodjaci.addItem(iObjekat);
        }
        else{
            JOptionPane.showMessageDialog(this, so.getPoruka(),"Greska", JOptionPane.ERROR_MESSAGE);
        }
    }
