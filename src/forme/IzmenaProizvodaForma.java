    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        boolean valid = ValidatorForme.getInstanca().validacijaPolja(txtNaziv, txtCena);
        if(!valid){
            JOptionPane.showMessageDialog(this, ValidatorForme.getInstanca().getPoruka(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
        double cena = 0;
        String naziv = txtNaziv.getText();
        valid=true;
        for (char c : naziv.toCharArray()) {
            if(isDigit(c))
                valid=false;
        }
        if(valid==false){
            JOptionPane.showMessageDialog(this, "Naziv ne sme sadrzati cifre", "Greska", JOptionPane.ERROR_MESSAGE);
            return; 
        }          
        try {
          cena = Double.parseDouble(txtCena.getText()); 
          if (cena==0)
              return;
        } catch (Exception e) {
          JOptionPane.showMessageDialog(this, "Cena mora biti broj", "Greska", JOptionPane.ERROR_MESSAGE);
          return;
        }        
        String opis = txtOpis.getText();
        Proizvodjac pr = (Proizvodjac) cmbProizvodjaci.getSelectedItem();
        
        Proizvod izmenjen = new Proizvod(proizvod.getProizvodID(), naziv, cena, opis, pr);
        
        ServerskiOdgovor so = Kontroler.getInstanca().izmeniProizvod(izmenjen);
    
        if(so.isUspesno()){
            JOptionPane.showMessageDialog(this, "Sistem je izmenio proizvod");
        }else{
            JOptionPane.showMessageDialog(this, "Sistem ne moze da izmeni proizvod", "Greska", JOptionPane.ERROR_MESSAGE);
 
        }
        
        
    }//GEN-LAST:event_btnIzmeniActionPerformed

