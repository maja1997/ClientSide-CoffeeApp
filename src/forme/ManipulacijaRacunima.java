    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int red = tblRacuni.getSelectedRow();
        ModelTabeleRacuna mtr = (ModelTabeleRacuna) tblRacuni.getModel();
        Racun r = mtr.dajRacun(red);
        int response = JOptionPane.showConfirmDialog(this, "Da li sigurno zelite da obrisete izabrani racun?", "Brisanje racuna", JOptionPane.YES_NO_OPTION);
        if (response == JOptionPane.OK_OPTION) {
            ServerskiOdgovor so = Kontroler.getInstanca().obrisiRacun(r);
            
            if (so.isUspesno()) {
                JOptionPane.showMessageDialog(this, so.getPoruka(), "Uspesno", JOptionPane.INFORMATION_MESSAGE);
                prikaziRacune();
            } 
            else {
                JOptionPane.showMessageDialog(this, so.getPoruka(), "Neuspesno", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

