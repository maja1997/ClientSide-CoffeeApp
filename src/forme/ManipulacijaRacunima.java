    private void srediTabelu() {
        String dat = txtDatum.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        try {
            Date datum = sdf.parse(dat);
            Racun r = new Racun();
            r.setDatumIzrade(datum);
            
            ServerskiOdgovor so = Kontroler.getInstanca().pretraziRacune(r);

            if(so.isUspesno()){
                List<IObjekat> listaRacuna = (List<IObjekat>) so.getOdgovor();
                if(listaRacuna.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Sistem nije pronasao racune po zadatim kriterijumima pretrage");
                    return;
                }
                ModelTabeleRacuna mtr = new ModelTabeleRacuna();
                mtr.setLista(listaRacuna);
                tblRacuni.setModel(mtr);
                JOptionPane.showMessageDialog(this, "Sistem je pronasao racune po zadatim kriterijumima pretrage");
            }
            else{
            JOptionPane.showMessageDialog(this, "Sistem nije pronasao racune po zadatim kriterijumima pretrage");
            }        
            
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Datum nije u dobrom formatu","Greska", JOptionPane.ERROR_MESSAGE);
        }    
    }

    private void prikaziRacune() {
            Racun r = new Racun();
            r.setDatumIzrade(null);
            
            ServerskiOdgovor so = Kontroler.getInstanca().pretraziRacune(r);

            if(so.isUspesno()){
                List<IObjekat> listaRacuna = (List<IObjekat>) so.getOdgovor();
                if(listaRacuna.isEmpty()){
                    return;
                }
                ModelTabeleRacuna mtr = new ModelTabeleRacuna();
                mtr.setLista(listaRacuna);
                tblRacuni.setModel(mtr);
            }
            else{
            JOptionPane.showMessageDialog(this, "Sistem nije pronasao racune");
            }        
            
           
    }

    
